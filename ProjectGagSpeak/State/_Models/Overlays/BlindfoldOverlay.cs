    // hardcoded so that it is always available, can't accidentally be deleted.
    public static readonly byte[] FallbackImagePNG =
    [
        0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, 0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52, 0x00,
